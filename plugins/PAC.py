fp_extract = ft.FilePicker(
    on_result=lambda e: _extrair_pac(Path(e.files[0].path)) if e.files else logger(t("cancelled"), color=COLOR_LOG_YELLOW)
)

fp_rebuild = ft.FilePicker(
    on_result=lambda e: _reconstruir_pac(Path(e.files[0].path)) if e.files else logger(t("cancelled"), color=COLOR_LOG_YELLOW)
)

