# AÇÕES DOS COMANDOS (SEM THREADING)
# ==============================================================================

def action_extract():
    path = pick_file_topmost(t("select_pac_file"), [("PAC files", "*.pac"), (t("all_files"), "*.*")])
    if not path:
        logger(t("cancelled"), color=COLOR_LOG_YELLOW)
        return
    logger(t("processing", name=os.path.basename(path)), color=COLOR_LOG_YELLOW)
    extrair_pac(path)


def action_rebuild():
    path = pick_file_topmost(t("select_list_file"), [("Text files", "*.txt"), (t("all_files"), "*.*")])
    if not path:
        logger(t("cancelled"), color=COLOR_LOG_YELLOW)
        return
    logger(t("processing", name=os.path.basename(path)), color=COLOR_LOG_YELLOW)
    reconstruir_pac(path)


