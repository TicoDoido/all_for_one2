# FUNÇÕES PRINCIPAIS (ADAPTADAS PARA USAR LOGGER)
# ==============================================================================

def extrair_pac(file_path):
    try:
        with open(file_path, "rb") as f:
            magic = f.read(4)
            if magic != b"PAC\x00":
                logger(t("invalid_file_magic"), color=COLOR_LOG_RED)
                return

            file_count = struct.unpack("<I", f.read(4))[0]

            pointers = []
            for _ in range(file_count):
                ptr = struct.unpack("<I", f.read(4))[0]
                pointers.append(ptr)

            out_dir = os.path.join(os.path.dirname(file_path), os.path.splitext(os.path.basename(file_path))[0])
            os.makedirs(out_dir, exist_ok=True)
            logger(t("extracting_to", path=out_dir), color=COLOR_LOG_YELLOW)

            f.seek(0, os.SEEK_END)
            total_size = f.tell()

            extracted_files_list = []

            for i in range(file_count):
                start = pointers[i]
                end = pointers[i + 1] if i < file_count - 1 else total_size
                size = end - start
                f.seek(start)
                data = f.read(size)

                ext = ".bin"
                if len(data) >= 3 and all(32 <= b < 127 for b in data[:3]):
                    try:
                        ext = "." + data[:3].decode("ascii").strip()
                    except:
                        pass

                file_name = f"{i+1:04}{ext}"
                extracted_files_list.append(file_name)

                out_path = os.path.join(out_dir, file_name)
                with open(out_path, "wb") as out_file:
                    out_file.write(data)
                logger(t("file_extracted", filename=file_name), color=COLOR_LOG_GREEN)

            list_path = out_dir + ".txt"
            with open(list_path, "w", encoding="utf-8") as list_file:
                for name in extracted_files_list:
                    list_file.write(name + "\n")

            logger(t("extraction_completed", path=out_dir), color=COLOR_LOG_GREEN)

    except FileNotFoundError:
        logger(t("file_not_found", file=file_path), color=COLOR_LOG_RED)
    except Exception as e:
        logger(t("error") + ": " + str(e), color=COLOR_LOG_RED)


def reconstruir_pac(list_path):
    base = os.path.splitext(list_path)[0]
    folder = base
    if not os.path.isdir(folder):
        logger(t("folder_not_found", folder=folder), color=COLOR_LOG_RED)
        return

    try:
        with open(list_path, "r", encoding="utf-8") as f:
            lines = [ln.strip() for ln in f.readlines() if ln.strip()]
    except Exception as e:
        logger(t("error") + ": " + str(e), color=COLOR_LOG_RED)
        return

    if not lines:
        logger(t("empty_list"), color=COLOR_LOG_RED)
        return

    missing = [name for name in lines if not os.path.isfile(os.path.join(folder, name))]
    if missing:
        logger(t("missing_files", files="\n".join(missing)), color=COLOR_LOG_RED)
        return

    file_count = len(lines)

    file_datas = []
    for name in lines:
        path = os.path.join(folder, name)
        with open(path, "rb") as rf:
            file_datas.append(rf.read())

    header_size = 4
    count_size = 4
    pointers_size = 4 * file_count
    base_data_offset = header_size + count_size + pointers_size

    pointers = []
    current = base_data_offset
    for data in file_datas:
        pointers.append(current)
        current += len(data)

    out_pac = base + "_mod.pac"
    if os.path.exists(out_pac):
        # Sem interação com o usuário – simplesmente sobrescreve (ou pode-se adicionar uma opção, mas manteremos simples)
        logger(t("replace_file_question", file=out_pac), color=COLOR_LOG_YELLOW)

    try:
        with open(out_pac, "wb") as out:
            out.write(b"PAC\x00")
            out.write(struct.pack("<I", file_count))
            for p in pointers:
                out.write(struct.pack("<I", p))
            for data in file_datas:
                out.write(data)
    except Exception as e:
        logger(t("error_writing_pac", error=str(e)), color=COLOR_LOG_RED)
        return

    logger(t("rebuild_completed", file=out_pac), color=COLOR_LOG_GREEN)


